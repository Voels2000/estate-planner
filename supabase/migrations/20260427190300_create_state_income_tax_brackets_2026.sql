--   For flat-rate states, one row covers both (use 'single' and duplicate for 'mfj')
--   For no-income-tax states, no rows inserted (engine returns $0 tax)
--
-- max_amount: NULL means "no upper limit" (top bracket)
--
-- Safe to re-run — uses ON CONFLICT DO UPDATE
-- ─────────────────────────────────────────────────────────────────

-- ── Step 1: Create table ──────────────────────────────────────────

CREATE TABLE IF NOT EXISTS state_income_tax_brackets (
  id             uuid    PRIMARY KEY DEFAULT gen_random_uuid(),
  state          text    NOT NULL,
  tax_year       integer NOT NULL,
  filing_status  text    NOT NULL CHECK (filing_status IN ('single', 'mfj')),
  min_amount     numeric NOT NULL,
  max_amount     numeric,          -- NULL = top bracket (no upper limit)
  rate_pct       numeric NOT NULL,
  created_at     timestamptz DEFAULT now(),
  UNIQUE (state, tax_year, filing_status, min_amount)
);

ALTER TABLE state_income_tax_brackets ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public read" ON state_income_tax_brackets
  FOR SELECT USING (true);

CREATE POLICY "Admin write" ON state_income_tax_brackets
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE id = auth.uid() AND role = 'admin'
    )
  );

